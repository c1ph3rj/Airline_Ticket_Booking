class InputData{
    public InputData(String userName, String eMail, String mobileNumber, int age, String gender) {
        this.userName = userName;
        this.eMail = eMail;
        this.mobileNumber = mobileNumber;
        this.age = age;
        this.gender = gender;
    }

    private String userName;
    private String eMail;
    private String mobileNumber;
    private int age;
    private String gender;
}